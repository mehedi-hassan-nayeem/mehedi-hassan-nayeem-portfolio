  <script>
    document.addEventListener('DOMContentLoaded', () => {
      const form = document.querySelector('form');
      const scriptURL = 'action.php'; // URL to your PHP script
      const msg = document.getElementById('cus_msg_form');

      form.addEventListener('submit', e => {
        e.preventDefault();
        fetch(scriptURL, {
            method: 'POST',
            body: new FormData(form)
          })
          .then(response => response.json())
          .then(data => {
            if (data.status === 'success') {
              msg.style.visibility = 'visible';
              msg.innerHTML = "Hi! " + document.getElementById('name').value + ", thanks for contacting me, I will reply to you very soon!";
              setTimeout(() => {
                msg.style.visibility = 'hidden';
                msg.innerHTML = "";
              }, 5000);
              form.reset();
            } else {
              msg.style.visibility = 'visible';
              msg.innerHTML = "Unable to submit form. Please try again later.";
              setTimeout(() => {
                msg.style.visibility = 'hidden';
                msg.innerHTML = "";
              }, 5000);
            }
          })
          .catch(error => {
            msg.style.visibility = 'visible';
            msg.innerHTML = "Unable to submit form. Please try again later.";
            setTimeout(() => {
              msg.style.visibility = 'hidden';
              msg.innerHTML = "";
            }, 5000);
          });
      });
    });
  </script>

